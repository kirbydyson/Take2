USE baseball;

CREATE TABLE IF NOT EXISTS users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    email VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    nameFirst VARCHAR(100),
    nameLast VARCHAR(100),
    role ENUM('user', 'admin') DEFAULT 'user',
    isBanned BOOLEAN DEFAULT FALSE
);

CREATE TABLE IF NOT EXISTS scoredle_games (
    id INT AUTO_INCREMENT PRIMARY KEY,
    userId INT NOT NULL,
    correctWord VARCHAR(255) NOT NULL,
    attemptCount INT NOT NULL,
    guessedWord BOOLEAN NOT NULL,
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (userId) REFERENCES users(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS trivia_game_results (
    id INT AUTO_INCREMENT PRIMARY KEY,
    userId INT NOT NULL,
    number_correct INT NOT NULL,
    played_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (userId) REFERENCES users(id) ON DELETE CASCADE
);


CREATE TABLE IF NOT EXISTS baseball_terms (
    id INT AUTO_INCREMENT PRIMARY KEY,
    term VARCHAR(100) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS queries (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(512) NOT NULL UNIQUE,
    query TEXT NOT NULL
);

CREATE TABLE TeamDivisionHistory (
    yearID INT NOT NULL,
    teamID VARCHAR(10) NOT NULL,
    divisionID VARCHAR(10) NOT NULL,
    PRIMARY KEY (yearID, teamID)
);

CREATE TABLE wordseries_games (
    id INT AUTO_INCREMENT PRIMARY KEY,
    userId INT NOT NULL,
    attemptsLeft INT NOT NULL,
    gameCompleted BOOLEAN NOT NULL,
    playedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (userId) REFERENCES users(id)
);

CREATE TABLE no_hitters (
    no_hitters_ID INT PRIMARY KEY AUTO_INCREMENT,
    Rk INT,
    Name VARCHAR(100),
    Prfct VARCHAR(5),
    Gcar INT,
    Gtm INT,
    yearID INT,
    Date VARCHAR(20),
    teamID CHAR(3),
    playerID VARCHAR(20)
);

